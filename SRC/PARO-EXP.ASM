	LD	BC,PSGIOR
	XOR	A
L_PSGSV:	;
	OUT	(PSGIOA),A
	INI
	INC	A
	CP	13
	JP	NZ,L_PSGSV
;
;	safty psg#7 jOy I/O mode
	LD	A,(PSG_SV+7)
	AND	3FH
	OR	80H
	LD	(PSG_SV+7),A	; 10XXXXXX
;
	LD	A,7
	OUT	(PSGIOA),A
	LD	A,0BFH
	OUT	(PSGIOW),A
;
	LD	A,3FH
	LD	(BANK2),A
	LD	HL,9800H
	LD	DE,SCCWAV
	LD	BC,80H
	LDIR
	XOR	A
	LD	(988FH),A
;
	XOR	A
	CALL	CHGCAP
;
	LD	DE,0704H	;ESC
L_ESC:	CALL	KEYIN
	JP	Z,L_ESC
S_CAP:	LD	DE,0
L_FUNC:	LD	HL,FC_TBL
L_FUNC_:	;
;MSX1–¢‘Î‰ž‚È‚Ì‚ÅvsyncŠÄŽ‹‘Ò‚¿‚Í‚µ‚È‚¢
