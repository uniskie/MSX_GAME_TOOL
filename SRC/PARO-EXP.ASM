	IN	A,(KEYIO+1)
	AND	0F0H
	OR	7
	OUT	(KEYIO+1),A
	IN	A,(KEYIO)
	AND	4	;ESC
	JP	NZ,TRAP_MS
;
	LD	(SVW_SP),SP
	LD	HL,(STACKA)
	LD	SP,HL
;
