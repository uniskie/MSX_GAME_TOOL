;
	INC	DE
	LD	A,D
	AND	20H	;2000H
;
	LD	A,1
	JR	NZ,J_CAP
	XOR	A
J_CAP:	LD	BC,(CAPSTA)
	CP	C
	LD	(CAPSTA),A
	CALL	NZ,CHGCAP
J_CAP_:	;
	PUSH	DE
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
	LD	A,E
	OR	D
	EX	AF,AF'	;AF':DE=0 ->LOOP
	INC	HL
	LD	C,(HL)
	INC	HL
	LD	B,(HL)
	INC	HL
	CALL	KEYIN
	POP	DE
	JP	NZ,L_FUNC_
	EX	AF,AF'	;AF':DE=0 ->LOOP
	LD	A,0
	CALL	NZ,CHGCAP
	LD	H,B
	LD	L,C
	JP	(HL)
;--------------
;
CAPSTA:	DW	0
;
RETURN@:	;
	JP	S_CAP
W_CAP:	DB	0
;
RETURN:	LD	DE,0704H	;ESC
L_RET:	CALL	KEYIN
	JP	Z,L_RET
;
	LD	HL,PSG_SV
	LD	BC,PSGIOW
	XOR	A
L_PSGLD:	;
	OUT	(PSGIOA),A
	OUTI
	INC	A
	CP	13
	JP	NZ,L_PSGLD
