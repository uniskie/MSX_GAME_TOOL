SAVSP:	EQU	SCCWAV+80H
CHGCAP:	EQU	00132H
;
V_PG1:	EQU	2
V_PG2:	EQU	4
V_PG3:	EQU	6
M_SIZE:	EQU	1CH
M_ADR:	EQU	0E000H
