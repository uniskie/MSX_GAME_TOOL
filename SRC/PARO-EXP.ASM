FC_TBL:	DW	0602H,CTRL	; CTRL
	DW	0501H,SAVE	;'S'
	DW	0402H,LOAD	;'L'
	DW	0301H,CURE	;'C'
	DW	0420H,PLAY	;'P'
	DW	0704H,RETURN	; ESC
	DW	0,L_FUNC	; END of DATA
;
CTRL:	LD	HL,FC_TBL@
	JP	L_FUNC_
FC_TBL@:	;
	DW	0120H,QUIT	;'@'
	DW	0501H,SAVE@Q	;'S'
	DW	0402H,LOAD@	;'L'
	DW	0710H,RESET	; STOP
	DW	0,L_FUNC	; END of DATA
;
;
SAVE@Q:	LD	A,V_PG2
	CALL	SAVE_
QUIT:	LD	A,V_PG1
	CALL	SAVE_
	LD	A,V_PG3
	CALL	LOAD_
	JP	RET_BAS	;COMMON
;
SAVE@:	LD	A,V_PG1
	JP	SAVE+2
SAVE:	LD	A,V_PG2
	LD	HL,RETURN@
	PUSH	HL
SAVE_:	INC	A
	LD	(SAVE__+1),A
	DEC	A
	LD	HL,M_ADR
	LD	B,M_SIZE
	LD	C,20H+40H
	CALL	TF_VRM
;
	LD	D,0
L_SAVE:	LD	C,D
	LD	HL,BUF
	LD	B,1
	LD	A,0
	CALL	TF_RAM
	LD	A,40H
	OR	D
	LD	C,A
	LD	HL,BUF
	LD	B,1
SAVE__:	LD	A,0	;vram page +1
	CALL	TF_VRM
;
	INC	D
	LD	A,40H
	SUB	A,D
	JP	NZ,L_SAVE
;
	RET
;
LOAD@:	LD	HL,(STACKA)
	LD	SP,HL
	LD	A,V_PG1
	JP	LOAD+2
LOAD:	LD	A,V_PG2
	LD	HL,RETURN@
	PUSH	HL
LOAD_:	LD	D,0
	LD	(LOAD__+1),A
	INC	A
	LD	(L_LOAD+1),A
L_LOAD:	LD	A,0	;vram page +1
	LD	HL,BUF
	LD	B,1
	LD	C,D
	CALL	TF_RAM
	LD	A,40H
	OR	D
	LD	C,A
	LD	HL,BUF
	LD	B,1
	LD	A,0
	CALL	TF_VRM
;
	INC	D
	LD	A,40H
	SUB	A,D
	JP	NZ,L_LOAD
;
	LD	HL,M_ADR
	LD	B,M_SIZE
	LD	C,20H
LOAD__:	LD	A,0	;vram page
	CALL	TF_RAM
;
	LD	A,(W_BANK+0)
	LD	(BANK1),A
	LD	A,(W_BANK+1)
	LD	(BANK2),A
	LD	A,(W_BANK+2)
	LD	(BANK3),A
	CALL	WR_CRG
	RET
;
;
RESET:	INC	A
	CALL	CHGCAP
;
	JP	PAR_ADR
