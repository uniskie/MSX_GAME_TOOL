;--------------------------------------
;*** CHANGE SLOT
;*** (ENASLT)
;
;IN:	HL=ADDRESS
;	A=SLOT
;USE:	ALL
;--------------------------------------
SLTTBL:	equ	0FCC5H ; Mirror of the four possible Secondary Slot Registers (4b)
SLOTIO:	equ	0A8H
EXSLTR:	equ	0FFFFH
;
CHGSLT:
	CALL	.MKSLTV
	JP	M,.CHGSLT_1
	; BASIC SLOT
	IN	A,(SLOTIO)
	AND	C
	OR	B
	OUT	(SLOTIO),A
	RET
.CHGSLT_1:
	; EXTEND SLOT
	PUSH	HL
	CALL	.CHGEXSLT
	LD	C,A
	LD	B,0
	LD	A,L
	AND	H
	OR	D		;SLOT TABLE VALUE
	LD	HL,SLTTBL
	ADD	HL,BC
	LD	(HL),A		;EX SLOT TABLE
	POP	HL
	LD	A,C
	JR	CHGSLT
;
	; MAKE SLOT REGISTER MASK AND VALUE
.MKSLTV:
	DI
	PUSH AF
	LD	A,H
	RLCA
	RLCA
	AND	03H	; HL BIT 15-14 -> A BIT 1-0
	LD	E,A	; E=PAGE
	LD	A,0C0H	; 1100 0000B
.MKSLTV_1:
	RLCA
	RLCA
	DEC	E
	JP	P,.MKSLTV_1
	LD	E,A		;E=PAGE MASK
	CPL
	LD	C,A		;REVERSE PAGE MASK
	POP	AF
	PUSH	AF
	AND	03H		;BASE SLOT
	INC	A
	LD	B,A
	LD	A,0ABH
.MKSLTV_2:
	ADD	A,55H
	DJNZ	.MKSLTV_2
	LD	D,A		; BASE SLOT NO.: 000000/01010101/10101010/11111111
	AND	E		; E=PAGE MASK
	LD	B,A		; B=VALUE / C=REVERSE PAGE MASK
	POP	AF
	AND	A
	RET
;
;	CHANGE EXTEND SLOT
.CHGEXSLT:
	PUSH	AF
	LD	A,D
	AND	0C0H
	LD	C,A
	POP	AF
	PUSH	AF
	LD	D,A
	IN	A,(SLOTIO)	;SLOT REG
	LD	B,A
	AND	3FH
	OR	C
	OUT	(SLOTIO),A	;CHANGE PAGE3 BASE SLOT (FOR MEMORY MAPPED I/O)
	LD	A,D
	RRCA
	RRCA
	AND	03H
	LD	D,A		;D=EX SLOT NO.
	LD	A,0ABH
.CHGEXSLT_1:
	ADD	A,55H
	DEC	D
	JP	P,.CHGEXSLT_1
	AND	E		; E=PAGE MASK
	LD	D,A		; EX SLOT NO.: 000000/010101/101010/111111
	LD	A,E
	CPL
	LD	H,A		; H=REVERSE PAGE MASK
	LD	A,(EXSLTR)
	CPL
	LD	L,A
	AND	H
	OR	D
	LD	(EXSLTR),A	;CHANGE EXTEND SLOT
	LD	A,B
	OUT	(SLOTIO),A	;CHANGE BASE SLOT
	POP	AF
	AND	03H
	RET
;
